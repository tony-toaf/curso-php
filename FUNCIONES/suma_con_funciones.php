<?php 


function suma($numero1, $numero2){
	$resultado = $numero1+$numero2;
	echo "El resultado de la suma es: $resultado <br>";
}



function resta($numero1, $numero2){
	$resultado = $numero1-$numero2;
	echo "El resultado de la resta es: $resultado <br>";
}



$n1 = 1;
$n2  = 3;

suma($n1, $n2);
resta($n1, $n2);



 ?>